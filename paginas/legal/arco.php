<p>Bajo la aplicación de la mexicana Ley Federal de Protección de datos Personales (LFPDDD) del 27 de abril de 2010, bajo la normativa europea Reglamento General de Protección de datos (RGPD, L 119, 4.5.2016, p. 1–88) del 27 de abril de 2016 y bajo la española Ley Orgánica 3/2018 del 5 de diciembre, Protección de Datos Personales y garantípia de los derechos digitales, se notifica que los clientes, usuarios y cualquier otro agente relacionado con Zendha Systems, tienen los derechos de acceso, rectificación, cancelación y oposición sobre sus datos. </p>

<h2>Para ejercer tus derechos ARCO</h2>
<p>Para ejercitar los derechos ARCO,  se debe enviar un correo electrónico a la dirección datos@zendha.net con una identificación oficial, y en caso de ser representante, un documento que avale su representación legal sobre el sujeto.</p>
